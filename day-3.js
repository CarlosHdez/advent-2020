const countTrees = (input, right, down) => {
  // console.log(input[0])
  let col = 0 // Moves along a string
  let trees = 0
  const mod = input[0].length
  input.slice(1).forEach((line, idx) => {
    if (down > 1 && !(idx % down)) {
      // console.log(line)
      return
    }
    col = (col + right) % mod
    // console.log(`${line} --- ${line.slice(0, col)}X${line.slice(col+1)}`)
    const point = line[col]
    if (point === '#') {
      trees++
    }
  })
  return trees
}

